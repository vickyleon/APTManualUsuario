             <article class="noColumna">
		<h1> Constitución de la AAPT-MX </h1>
		<h2> ARTÍCULO I: NOMBRE </h2>
			<p>El nombre de la Asociación es “Asociación Americana de Profesores de Física,
			Sección México”. En todas sus actividades, se referirá a ella como la “AAPT-MX”.
			</p>
		<h2> ARTÍCULO II: OBJETIVOS </h2>
			<p>
			Los objetivos de la Asociación serán mejorar la calidad de la educación en la Física
			en el país, incrementar la presencia del rol que cumplen las diversas actividades
			realizadas dentro de la Física en nuestro entorno y  mantener una comunicación
			efectiva entre todos aquellos que se dediquen a la educación en Física, en todos
			los niveles académicos. El trabajo de esta Asociación será estrictamente para
			fines educativos, tecnológicos y científicos.
			</p>
		<h2> ARTÍCULO III: MEMBRESÍA </h2>
			<p>
			Existirán cuatro tipos diferentes de membresía: la membresía regular, la membresía estudiantil, la membresía emérita y la membresía honoraria.
				<ol>
				<li>La membresía regular se concederá a profesores de física en los todos los niveles educativos y a cualquier otra persona que, a juicio del Consejo (artículo VI), contribuya al cumplimiento de los objetivos de esta Asociación.</li>
				<li>La membresía estudiantil se concederá a estudiantes universitarios inscritos en cualquier institución educativa a partir de su segundo año de su programa de estudios.</li>
				<li>Serán miembros eméritos aquellas personas que sean miembros regulares antes de jubilarse y que, antes de su retiro, soliciten esta membresía al Consejo.</li>
				<li>Será prerrogativa del Consejo otorgar membresías honorarias. Los poseedores de dicha membresía tendrán todos los privilegios de un miembro regular, sin tener que cubrir ningún tipo de cuota.</li>
				</ol>

			</p>
		<h2> ARTÍCULO IV: DIRECTIVOS </h2>
			<p>Los directivos de la Asociación serán: Un Presidente, un Presidente Electo, un Vicepresidente, un Consejero asesor y un Tesorero.
				<ol>
					<li>El presidente será responsable de los asuntos que conciernen a la Asociación y deberá presidir todas las reuniones
					del Consejo, así como cualquier otra reunión de los directivos o de la Asociación. El presidente tendrá el poder de nombrar
					comités y remplazar a los miembros del Consejo según lo descrito en los artículos VI y VII de este documento. El Presidente
					será quien autorice los gastos de los fondos de la Asociación. El periodo de mandato de un Presidente iniciará al final de 
					la junta anual y durará un año. Después  ocupará el puesto de Consejero asesor.</li>
					<li>El Presidente Electo asumirá la función de co-presidir en todas las reuniones y será el coordinador general de las 
					reuniones anuales de la sección. El Presidente Electo podrá tener otras funciones si el Consejo lo determina. Ocupará este
					cargo hasta el final de la junta anual, cuando asumirá el puesto de Presidente.</li>
					<li>El Consejero asesor será el representante oficial de la AAPT-MX frente a todas las sociedades designadas por el Consejo.
					El Consejero asesor presidirá el Comité de Nominaciones, y podrá tener otras funciones si lo decide así el Consejo.
					</li>
					<li>El Vicepresidente apoyará al Presidente y al Presidente Electo en sus funciones, y podrá tener otras funciones si así
					lo decide el Consejo. El Vicepresidente tendrá este puesto durante un año, hasta el final de la junta anual, cuando tomará
					el puesto de Presidente Electo.
					</li>
					<li>5.	El Tesorero tendrá todas las funciones relacionadas con las finanzas de la Sección, en particular, deberá recolectar
					las cuotas de los miembros, será quien administre los fondos y cubra los gastos generados por la Sección; deberá mantener un
					registro de todos los gastos e ingresos de fondos y presentar un reporte del estado financiero de la Sección en cada junta
					regular. El Tesorero tendrá este puesto durante cuatro años, empezando al final de la junta anual que corresponda y podrá ser reelegido.
					</li>
				</ol>
			</p>
		<h2> ARTÍCULO V: REPRESENTANTE DE SECCIÓN </h2>
			<p>
			El Representante de la Sección en el Consejo será responsable de representar a la Sección en todas las juntas de la AAPT y de  servir
			como medio de comunicación entre la Sección y la AAPT. Además, fungirá como secretario del Sección y deberá llevar los registros de la
			Sección. Podrá tener otras funciones si el Consejo de la Sección así lo dispone.<br>
			El Representante de la Sección dirigirá el Comité de Premiación.<br>
			Si el Representante de Sección no puede asistir a una junta de Representantes de Sección de la AAPT, deberá designar un sustituto quien
			deberá ser miembro de la Sección y de la AAPT. El Representante deberá notificar este cambio al Secretario de la AAPT por escrito antes
			de la junta. El Representante de la Sección tendrá este puesto durante cuatro años, empezando al final de la junta anual que corresponda
			y podrá ser reelegido. <br>

			</p>
		<h2> ARTÍCULO VI: EL CONSEJO </h2>
			<p>
			La Asociación será gobernada por un Consejo que consistirá de los directivos, el Representante de Sección y 3 miembros generales quienes
			además deberán ser miembros de la AAPT. Los miembros generales tomarán el cargo en la junta anual, después de las elecciones, por un 
			periodo de 3 años, de tal manera que un miembro sea elegido cada año. Los miembros generales podrán ser reelegidos. Los tres miembros
			generales deben ser representantes de cada uno de los siguientes niveles educativos: educación superior, media superior y básica. Si un
			miembro del Consejo no puede fungir sus funciones deberá ser reemplazado, dicha acción requiere que dos terceras partes del Consejo estén
			de acuerdo. En el caso de que algún oficial o miembro del Consejo se vea imposibilitado de cumplir temporalmente sus funciones, el 
			Presidente está facultado a nombrar un sustituto temporal. <br>
			El quórum se definirá como la mayoría del Consejo, es decir cinco miembros de nueve que forman el consejo.
			</p>
		<h2> ARTÍCULO VII: COMITÉS </h2>
			<p>
				Existirán dos comités: El Comité de Nominaciones al Consejo y el Comité de Premiación. <br>
				El Comité de Nominaciones será presidido por el Consejero asesor, e incluye miembros representantes de los tres grupos mencionados
				en el Artículo VI, nominados por el Presidente y aprobados por el Consejo. El Comité de Nominaciones deberá recibir los resultados
				de la convocatoria realizada por el Presidente y, usando esta información y su propio criterio, presentar una lista de posiciones
				disponibles y sus candidatos al Presidente. El Comité de Nominaciones tiene la responsabilidad de asegurarse de que los candidatos
				propuestos estén de acuerdo en asumir los cargos para los que han sido nominados. El Presidente se asegurará de que todos los miembros
				estén al tanto de esta información al menos tres meses antes de la junta anual. <br>
				El Comité de Premiación será presidido por el Representante de Sección e incluirá a otros dos miembros escogidos y aprobados por el Consejo.<br>
				El Presidente y el Consejo nombrarán otros comités con el número de miembros que ellos consideren pertinente para el posterior 
				desarrollo de la Asociación. Dichos comités dejarán de existir, a más tardar, en la junta anual. Los comités podrán ser reestablecidos.

			</p>
		<h2> ARTÍCULO VIII: ELECCIONES </h2>
			<p>
				Los directivos, miembros generales del Consejo y el Representante de Sección deberán ser elegidos por la mayoría de los miembros
				de la Asociación, presentes y votando en la junta anual. Los candidatos a cualquier posición deberán ser miembros de la Sección,
				nominados por el Comité de Nominaciones o directamente en una junta o por petición escrita del Presidente, firmada por al menos
				diez miembros de la Sección, máximo 2 meses antes de la elección. La pluralidad de votos asegura la elección. En caso de empate, 
				el Presidente decidirá quién es el ganador al azar. 
			</p>
		<h2> ARTÍCULO IX: CUOTAS </h2>
			<p>
				Las cuotas serán establecidas por el Consejo de Sección y se usarán para cubrir los gastos generados por la Asociación. El año fiscal 
				de la Asociación comenzará el 1° de Mayo.
			</p>
		<h2> ARTÍCULO X: JUNTAS </h2>
			<p>
				Deberá haber una junta anual, organizada por el Consejo. Todos los miembros de la Asociación deberán ser notificados oportunamente
				sobre la fecha, la hora y el lugar. El Consejo, junto con los presidentes, fijarán las cuotas de registro.
			</p>
		<h2> ARTÍCULO XI: NO DISCRIMINACIÓN </h2>
			<p>
				La Asociación no discriminará por raza, religión, color, nacionalidad o sexo.
			</p>
		<h2> ARTÍCULO XII: ORGANIZACIÓN SIN FINES DE LUCRO </h2>
			<p>
				<ol>
					<li>Sin contravenir ninguna disposición de la Constitución, esta Asociación no deberá realizar ninguna actividad con fines de lucro.</li>
					<li>Esta asociación continuará indefinidamente a menos que dos terceras partes de los miembros voten a favor de disolver la Asociación. 
					Dicha votación deberá ser llevada a cabo siguiendo las indicaciones del Consejo. Las propuestas para disolver la Asociación pueden ser 
					puestas a votación en cualquier momento. En el caso de que la Asociación sea disuelta, el Consejo, después de cubrir las cuentas pendientes,
					distribuirá los fondos restantes entre organizaciones educativas, científicas o de caridad.</li>
				</ol>
			</p>
		<h2> ARTÍCULO XIII:  MODIFICACIONES </h2>
			<p> Esta Constitución será modificada siempre y cuando:
				<ol>
					<li>Dos terceras partes de los miembros voten a favor y siempre y cuando los miembros hayan recibido información sobre la
					modificación al menos dos semanas antes de la junta donde se realizará la votación.</li>
					<li>O que dos terceras partes de los miembros voten a favor en una votación por correo, organizada por el Consejo.</li>
				</ol>
				Adoptada por la Sección el 9 de agosto de 2008
			</p>
		
